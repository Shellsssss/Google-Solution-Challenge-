export type Translations = typeof T.en;

export function useT(): Translations {
  const language = useAppStore((s) => s.language) as Lang;
  return (T[language] ?? T.en) as unknown as Translations;
}

export function t(lang: Lang, key: keyof Translations): string {
  const translations = (T[lang] ?? T.en) as unknown as Record<string, unknown>;
  const val = translations[key];
  return typeof val === 'string' ? val : String(val ?? key);
}

